                    <h3>Pengertian Logaritma</h3>
                    <p>
                        Secara sederhana, logaritma dapat disebut sebagai kebalikan dari perpangkatan atau eksponensial.
                        Misalkan terdapat suatu perpangkatan atau eksponensial berbentuk a<sup>c</sup> = b, kebalikan
                        dari perpangkatan tersebut dapat
                        disajikan dalam bentuk logaritma <sup>a</sup>log b = c dengan syarat a ≠ 0 dan a > 1. <br />
                        Beberapa bentuk penulisan logaritma yaitu <sup>a</sup>log b = c atau log<sub>a</sub> b = c.
                        Kedua bentuk penulisan logaritma tersebut adalah sama.
                    </p>
                </div>
                <div class="row">
                    <h3>Logaritma dalam Kehidupan Sehari-hari</h3>
                    <p>
                        Logaritma banyak dimanfaatkan dalam kehidupan sehari-hari. Dahulu sebelum adanya kalkulator,
                        logaritma dimanfaatkan dalam perhitungan eksponensial. <br /><br />
                        Selain itu terdapat manfaat lain dari konsep logaritma. Konsep logaritma digunakan dalam
                        perhitungan seismograf atau alat pengukur kekuatan gempa. <br /><br />
                        Satuan skala richter menggunakan konsep logaritma dalam perhitungannya. Dalam bidang astronomi
                        juga digunakan dalam perhitungan untuk mengukur tingkat keterangan suatu bintang.
                    </p>
                </div>
                <div class="row">
                    <h3>Rumus Logaritma</h3>
                    <p>
                        Pada bagian sebelumnya kalian telah mengetahui bentuk logaritma. <br /><br />
                        Bentuk logaritma dinyatakan dengan <sup>a</sup>log b = c. <br /><br />
                        Simbol a menyatakan bilangan pokok logaritma atau basis, b menyatakan range atau hasil dari
                        logaritma, dan c merupakan domain logaritma.
                    </p>
                </div>
                <div class="row">
                    <h3>Sifat Logaritma</h3>
                    <p>Berikut merupakan beberapa sifat logaritma.</p>
                    <li><sup>a</sup>log a = 1</li>
                    <li><sup>a</sup>log 1 = 0</li>
                    <li><sup>a^n</sup>log b<sup>m</sup> = (m/n) x <sup>a</sup>log b</li>
                    <li><sup>a^m</sup>log b<sup>m</sup> = <sup>a</sup>log b</li>
                    <li><sup>a</sup>log b = <1> <sup>b</sup>log a</li>
                    <li><sup>a</sup>log b = (<sup>k</sup>log b)/(<sup>k</sup>log a)</li>
                    <li>a^(<sup>a</sup>log b) = b</li>
                    <li><sup>a</sup>log b + <sup>a</sup>log c = <sup>a</sup>log (bc)</li>
                    <li><sup>a</sup>log b – <sup>a</sup>log c = <sup>a</sup>log (b/c)</li>
                    <li><sup>a</sup>log b . <sup>b</sup>log c = <sup>a</sup>log c</li>
                    <li><sup>a</sup>log (b/c) = – <sup>a</sup>log (c/b)</li>
                </div>
                <div class="row">
                    <h3>PersamaanLogaritma</h3>
                    <p>
                        Bagian ini akan dibahas mengenai persamaan logaritma. <br /><br />
                        Misalkan terdapat <sup>a</sup>log f(x) dan <sup>a</sup>log g(x). <br /><br />
                        Kedua bentuk tersebut dikatakan sama atau <sup>a</sup>log f(x) = <sup>a</sup>log g(x) jika f(x)
                        = g(x) dengan syarat a ≠ 1, a > 0, f(x) > 0, dan g(x) > 0.
                    </p>
                </div>
                <div class="row">
                    <h3>Pertidaksamaan Logaritma</h3>
                    <p>
                        Misalkan terdapat <sup>a</sup>log f(x) dan <sup>a</sup>log g(x). Dari kedua bentuk tersebut
                        <sup>a</sup>log f(x) > <sup>a</sup>log g(x) jika a > 0 maka f(x) > g(x). Jika 0 < a< 1 maka f(x)
                            < g(x). </p>
                </div>
