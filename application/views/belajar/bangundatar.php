                    <h3>Contoh Soal Bangun Datar</h3>
                    <p>
                        1. Tentukan luas bangun datar berikut.
                        <li>Persegi dengan ukuran sisi 8 cm.</li>
                        <li>Persegi panjang dengan ukuran panjang 12 cm dan lebar 4 cm.</li>
                        <li>Segitiga dengan ukuran alas 7 cm dan tinggi 6 cm.</li>
                        <li>Jajar genjang dengan ukuran alas 5 cm dan tinggi 8 cm.</li>
                        <li>Trapesium dengan ukuran sisi sejajar 12 cm dan 6 cm, serta tinggi trapesium 5 cm.</li>
                        <li>Belah ketupat dengan ukuran diagonalnya 12 cm dan 9 cm.</li>
                        <li>Layang-layang dengan ukuran diagonalnya 6 cm dan 14 cm.</li>
                        <li>Lingkaran dengan jari-jari 14 cm.</li>
                    </p>
                    <div class="card">
                        <h5 class="card-header">Pembahasan</h5>
                        <div class="card-body">
                            <p class="card-text">
                                <li>Persegi</li>
                            <p>
                                L = s x s <br>
                                L = 8 cm x 8 cm = 64 cm2.
                            </p>
                            <li>Persegi Panjang</li>
                            <p>
                                L = p x l <br>
                                L = 12 cm x 4 cm = 48 cm2.
                            </p>
                            <li>Segitiga</li>
                            <p>
                                L = ½ x a x t <br>
                                L = ½ x 7 cm x 6 cm <br>
                                L = ½ x 42 cm2 = 21 cm2.
                            </p>
                            <li>Jajargenjang</li>
                            <p>
                                L = a x t <br>
                                L = 5 cm x 8 cm = 40 cm2.
                            </p>
                            <li>Trapesium</li>
                            <p>
                                L = ½ x (a + b) x t <br>
                                L = ½ x (12 cm + 6 cm) x 5 cm <br>
                                L = ½ x 18 cm x 5 cm <br>
                                L = 9 cm x 5 cm = 45 cm2.
                            </p>
                            <li>Belah ketupat</li>
                            <p>
                                L = ½ x d1 x d2 <br>
                                L = ½ x 12 cm x 9 cm <br>
                                L = 6 cm x 9 cm <br>
                                L = 54 cm2
                            </p>
                            <li>Layang-layang</li>
                            <p>
                                L = ½ x d1 x d2 <br>
                                L = ½ x 6 cm x 14 cm <br>
                                L = 3 cm x 14 cm <br>L = 42 cm2
                            </p>
                            <li>Lingkaran</li>
                            <p>
                                L = π x r x r <br>
                                L = (22/7) x 14 cm x 14 cm <br>
