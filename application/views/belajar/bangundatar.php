                    <h3>Pengertian Bangun Datar</h3>
                    <p>
                        Bangun datar merupakan salah satu topik yang mempelajari objek atau bentuk berbentuk dua
                        dimensi. <br><br>
                        Bangun dua dimensi merupakan bangun yang memiliki keliling dan luas, tetapi tidak memiliki isi
                        (volume). Bangun datar banyak diterapkan dalam kehidupan sehari-hari.
                    </p>
                </div>
                <div class="row">
                    <h3>Bangun Datar dalam Kehidupan Sehari-Hari</h3>
                    <p>
                        Beberapa contoh penerapannya yaitu bentuk ubin yang menyerupai bangun persegi dan sisi meja
                        menyerupai bentuk persegi panjang.
                    </p>
                </div>
                <div class="row">
                    <h3>Macam-Macam Bangun Datar</h3>
                    <div class="text-center">
                        <img src="<?= base_url('assets/'); ?>images/datar1.jpg">
                    </div>
                    <p>
                        terdapat macam-macam bangun datar seperti bangun persegi, persegi panjang, segitiga, jajar
                        genjang, trapesium, belah ketupat, layang-layang, dan lingkaran.
                    </p>
                </div>
                <div class="row">
                    <h3>Sifat-Sifat Bangun Datar</h3>
                    <h5>1. Persegi</h5>
                    <div class="text-center">
                        <img src="<?= base_url('assets/'); ?>images/datar2.jpg" alt="">
                    </div>
                    <p>
                        Sifat-sifat persegi yaitu sebagai berikut.
                        <li>Memiliki empat sisi yang sama panjang (dua pasang sisi yang sejajar).</li>
                        <li>Mempunyai empat sudut siku-siku.</li>
                        <li>Memiliki dua diagonal yang saling berpotongan tegak lurus.</li>
                    </p>
                </div>
                <div class="row">
                    <h5>2. Persegi Panjang</h5>
                    <div class="text-center">
                        <img src="<?= base_url('assets/'); ?>images/datar3.jpg" alt="">
                    </div>
                    <p>
                        Sifat-sifat persegi panjang yaitu sebagai berikut.
                        <li>Memiliki dua pasang sisi yang sejajar dan sama panjang.</li>
                        <li>Keempat sudutnya siku-siku.</li>
                        <li>Memiliki dua diagonal yang sama panjang.</li>
                    </p>
                </div>
                <div class="row">
                    <h5>3. Segitiga</h5>
                    <div class="text-center">
                        <img src="<?= base_url('assets/'); ?>images/datar4.jpg" alt="">
                    </div>
                    <p>
                        Berdasarkan panjang sisinya, bangun datar segitiga dibedakan menjadi tiga, yaitu segitiga sama
                        sisi, segitiga sama kaki, dan segitiga sembarang. <br><br>
                        a. Segitiga Sama Sisi <br>
                        Sifat-sifat segitiga sama sisi yaitu sebagai berikut.
                        <li>Ketiga sisinya sama panjang.</li>
                        <li>Ketiga sudutnya sama besar (60°).</li><br>
                    </p>
                    <p>
                        b. Segitiga Sama Kaki <br>
                        Sifat-sifat segitiga sama kaki yaitu sebagai berikut.
                        <li>Dua dari tiga sisinya sama panjang.</li>
                        <li>Memiliki sepasang sudut yang sama besar.</li>
                    </p>
                    <p>
                        c. Segitiga Sembarang <br>
                        Sifat-sifat segitiga sembarang yaitu sebagai berikut.
                        <li>Ketiga sisinya tidak sama panjang</li>
                        <li>Ketiga sudutnya tidak sama besar</li>
                    </p>
                    <p>
                        Berdasarkan besar sudutnya, bangun datar segitiga dibedakan menjadi tiga, yaitu segitiga sama
                        siku-siku, segitiga lancip, dan segitiga tumpul. <br><br>
                        a. Segitiga Siku-Siku <br>
                        Sifat-sifat segitiga siku-siku yaitu sebagai berikut.
                        <li>Memiliki sudut terbesarnya adalah sudut siku-siku (90 derajat).</li>
                    </p>
                    <p>
                        b. Segitiga Lancip <br>
                        Sifat-sifat segitiga lancip yaitu sebagai berikut.
                        <li>Ketiga sudutnya merupakan sudut lancip.</li>
                    </p>
                    <p>
                        c. Segitiga Tumpul <br>
                        Sifat-sifat segitiga tumpul yaitu sebagai berikut.
                        <li>Salah satu sudutnya merupakan sudut tumpul.</li>
                    </p>
                </div>
                <div class="row">
                    <h5>4. Jajar Genjang</h5>
                    <div class="text-center">
                        <img src="<?= base_url('assets/'); ?>images/datar5.jpg" alt="">
                    </div>
                    <p>
                        Sifat-sifat jajar genjang yaitu sebagai berikut.
                        <li>Memiliki dua pasang sisi yang sejajar dan berhadapan sama panjang.</li>
                        <li>Memiliki dua pasang sudut yang berhadapan sama besar.</li>
                        <li>Memiliki dua diagonal yang membagi jajar genjang menjad dua sama besar.</li>
                    </p>
                </div>
                <div class="row">
                    <h5>5. Trapesium</h5>
                    <div class="text-center">
                        <img src="<?= base_url('assets/'); ?>images/datar6.jpg" alt="">
                    </div>
                    <p>
                        Sifat-sifat trapesium yaitu sebagai berikut.
                        <li>Memiliki sepasang sisi sejajar</li>
                        <li>Memiliki dua pasang sudut sama besar (trapesium sama kaki) atau memiliki dua sudut siku-siku
                            (trapesium siku-siku).</li>
                        <li>Jumlah besar sudut yang berdekatan di antara dua garis sejajar adalah 180 derajat.</li>
                    </p>
                </div>
                <div class="row">
                    <h5>6. Layang-Layang</h5>
                    <div class="text-center">
                        <img src="<?= base_url('assets/'); ?>images/datar7.jpg" alt="">
                    </div>
                    <p>
                        Sifat-sifat layang-layang yaitu sebagai berikut.
                        <li>Memiliki sepasang sudut yang sama besar.</li>
                        <li>Memiliki dua pasang sisi yang sama panjang.</li>
                    </p>
                </div>
                <div class="row">
                    <h5>7. Belah Ketupat</h5>
                    <div class="text-center">
                        <img src="<?= base_url('assets/'); ?>images/datar8.jpg" alt="">
                    </div>
                    <p>
                        Sifat-sifat belah ketupat yaitu sebagai berikut.
                        <li>Memiliki empat sisi yang sama panjang.</li>
                        <li>Memiliki dua pasang sudut yang berhadapan sama besar.</li>
                        <li>Diagonalnya saling berpotongan tegak lurus.</li>
                    </p>
                </div>
                <div class="row">
                    <h5>8. Lingkaran</h5>
                    <div class="text-center">
                        <img src="<?= base_url('assets/'); ?>images/datar9.jpg" alt="">
                    </div>
                    <p>
                        <li>Memiliki satu titik pusat.</li>
                        <li>Jarak sembarang titik pada lingkaran terhadap pusat adalah sama.</li>
                    </p>
