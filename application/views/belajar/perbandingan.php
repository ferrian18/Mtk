                    <h3>Pengertian Perbandingan</h3>
                    <p>
                        Perbandingan dalam matematika dapat disebut juga sebagai rasio.<br />
                        Lalu, apa itu perbandingan atau rasio? <br />
                        Perbandingan (rasio) merupakan merupakan salah satu teknik atau cara dalam membandingkan dua
                        besaran. <br />
                        Penulisan rasio atau perbandingan dapat dituliskan sebagai a : b atau a/b dengan a dan b
                        merupakan dua besaran yang memiliki satuan yang sama.
                    </p>
                </div>
                <div class="row">
                    <h3>Perbandingan dalam Kehidupan Sehari-hari</h3>
                    <p>Misalnya perbandingannya adalah 2 : 1, artinya untuk membuat roti tersebut diperlukan tepung
                        terigu 2 bagian dan tepung tapioca 1 bagian.</p>
                </div>
                <div class="row" data-aos="fade-left">
                    <h3>Perbandingan Senilai</h3>
                    <p>
                        Contoh perbandingan senilai yaitu perbandingan banyaknya tepung dengan banyaknya roti yang
                        dibuat. <br />
                        Semakin banyak tepung yang digunakan maka akan semakin banyak pula roti yang dibuat, begitu pula
                        sebaliknya.
                    </p>
                </div>
                <div class="row">
                    <h3>Perbandingan Berbalik Nilai</h3>
                    <p>
                        Perbandingan berbalik nilai merupakan antara dua variabel.<br />
                        Misalnya perbandingan antara ukuran gigi mesin bermotor dengan kecepatan. Ukuran gigi mesin
                        bermotor yang kecil akan menghasilkan kecepatan yang besar, begitu pula sebaliknya.
                    </p>
                </div>
                <div class="row">
                    <h3>Perbandingan Bertingkat</h3>
                    <p>
                        Contoh permasalahan berkaitan dengan perbandingan bertingkat misalnya perbandingan kelereng
                        Abdul dan Beni adalah 3 : 5, sedangkan perbandingan kelereng Beni dengan Ciko adalah 4 :
                        3.<br />
                        Untuk menyelesaikan permasalahan tersebut perlu menentukan rasio atau perbandingan dari kelereng
                        Abdul, Beni, dan Ciko.
                    </p>
                </div>
                <div class="row">
                    <h3>Cara Menghitung Perbandingan</h3>
                    <p>Cara yang dapat dilakukan untuk menghitung perbandingan yaitu sebagai berikut. <br><br>
                        1. Buatlah model dari permasalahan yang akan diselesaikan. <br>
                        2. Tentukan jenis perbandingan yang akan diselesaikan. Jenis perbandingan dapat berupa
                        perbandingan senilai, perbandingan berbalik nilai, perbandingan bertingkat, atau jenis yang
                        lainnya. <br>
                        3. Susun persamaan dan hitunglah perbandingannya untuk menentukan informasi yang ingin diperoleh
                        dengan menggunakan rumus perbandingan.
                    </p>
