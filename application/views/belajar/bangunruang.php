                    <h3>Macam-Macam Bangun Ruang</h3>
                    <p>
                        Bangun ruang memiliki beberapa macam. Berdasarka bentuknya, bangun ruang dibagi menjadi dua,
                        yaitu bangun ruang sisi datar dan bangun ruang sisi lengkung. <br><br>
                        Bangun ruang sisi datar meliputi, kubus, balok, prisma, dan limas. Bangun ruang sisi lengkung
                        meliputi, tabung, kerucut, dan bola.
                    </p>
