                <div class="row">
                    <h3>Contoh Soal Bangun Ruang</h3>
                    <p>
                        1. Sebuah balok berukuran panjang 12 cm, lebar 9 cm, dan tinggi 5 cm. Volume balok tersebut
                        adalah . . . .
                    </p>
                    <div class="card">
                        <h5 class="card-header">Pembahasan</h5>
                        <div class="card-body">
                            <p class="card-text">V = p x l x t <br>
                                V = 12 cm x 9 cm x 5 cm <br>
                                V = 540 cm3
                            </p>
                        </div>
                    </div>
                </div>
                <div class="row">
                    <p>
                        2. Suatu kubus memiliki ukuran rusuk 12 cm. Volume kubus tersebut adalah . . . .
                    </p>
                    <div class="card">
                        <h5 class="card-header">Pembahasan</h5>
                        <div class="card-body">
                            <p class="card-text">V = r x r x r<br>
                                V = 12 cm x 12 cm x 12 cm<br>
                                V = 1.728 cm3
                            </p>
                        </div>
                    </div>
                </div>
                <div class="row">
                    <p>
                        3. Sebuah kerucut dimasukkan ke dalam tabung sehingga puncak kerucut menyinggung tutup tabung.
                        Jika ukuran alas kerucut dan tabung sama. Tentukan perbandingan volume kerucut dengan volume
                        tabung.
                    </p>
                    <div class="card">
                        <h5 class="card-header">Pembahasan</h5>
                        <div class="card-body">
                            <p class="card-text">V kerucut/V tabung = (1/3 x π x r x r x t)/( π x r x r x t) = 1/3 <br>
                                Jadi, perbandingan volume kerucut dengan volume tabung adalah 1 : 3.
                            </p>
                        </div>
                    </div>
                </div>
                <div class="row">
                    <p>
                        4. Suatu bola memiliki ukuran jari-jari 3 cm. Jika ukuran jari-jari diperbesar menjadi dua kali
                        jari-jari semula, maka berapa kali volume bola sekarang dari volume bola sebelum diperbesar?
                    </p>
                    <div class="card">
                        <h5 class="card-header">Pembahasan</h5>
                        <div class="card-body">
                            <p class="card-text">V awal = 4/3 x π x 3 x 3 x 3 = 36 π <br>
                                V akhir = 4/3 x π x 6 x 6 x 6 = 288 π <br>
                                Volume akhir merupakan 8 kali volum bola mula-mula (sebelum diperbesar).
                        </div>
