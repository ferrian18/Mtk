                    <h3>Contoh Soal Aritmetika</h3>
                    <p>
                        1. Suatu mobil menempuh perjalanan dari kota A ke kota B dengan kecepatan 100km/jam selama 8
                        jam. Berapa lama waktu yang ditempuh jika mobil melaju dengan kecepatan 150km/jam?
                    </p>
                    <div id="pembahasan" class="border border-primary">
                        <h5>Pembahasan</h5>
                        <p>
                            Kecepatan = jarak ÷ waktu, <br><br>
                            maka: <br><br>
                            Waktu = Jarak ÷ Kecepatan <br>
                            Kecepatan = 150km/jam <br><br>
                            <strong>Jarak dari kota A ke B adalah </strong><br><br>
                            Jarak = Kecepatan × waktu <br>
                            Jarak = 100km/jam × 8 jam <br>
                            Jarak = 800km <br><br>
                            <strong>Maka waktu yang diperlukan jika kecepatannya 150km/jam adalah</strong> <br><br>
                            Waktu = Jarak ÷ Kecepatan <br>
                            Waktu = 800km ÷ 150km/jam <br>
                            Waktu = 5 ⅓ Jam <br><br>
                            Jadi, waktu yang ditempuh jika mobil melaju dengan kecepatan 150km/jam adalah 5 ⅓ Jam atau 5
                            jam 20 menit.
                        </p>
                    </div>
                </div>
                <div class="row">
                    <p>
                        2. Lia menghabiskan uang sejumlah Rp240.000 untuk membeli 40 buah Jeruk untuk dijual kembali.
                        Jika Lia ingin mendapatkan keuntungan sebesar Rp80.000, berpakah keuntungan dari menjual 1 buah
                        jeruk tersebut?
                    </p>
                    <div class="border border-primary">
                        <h5>Pembahasan</h5>
                        <p>
                            Karena <br>
                            Keuntungan = Harga Jual – Harga Beli <br>
                            Maka <br>
                            Keuntungan 1 Jeruk = Harga Jual 1 Jeruk – Harga Beli 1 Jeruk <br><br>
                            <strong>Hitung harga beli 1 jeruk</strong> <br><br>
                            Harga Beli 1 Jeruk = Harga Beli ÷ Jumlah Jeruk <br>
                            Harga Beli 1 Jeruk = Rp240.000 ÷ 40 = Rp6.000 <br><br>
                            <strong>Hitung harga jual 1 jeruk</strong><br><br>
                            Harga jual 1 jeruk = Harga jual ÷ Jumlah Jeruk <br>
                            Harga Jual = Harga Beli + Keuntungan <br>
                            Harga Jual = Rp240.000 + Rp80.000 <br>
                            Harga Jual = Rp320.000 <br>
                            Harga jual 1 jeruk = Rp320.000 ÷ 40 = Rp8.000 <br><br>
                            <strong>Sehingga diperoleh</strong> <br><br>
                            Keuntungan 1 Jeruk = Rp8.000 – Rp6.000 = Rp2.000 <br>
                            Jadi, keuntungan dari menjual 1 buah jeruk tersebut adalah Rp2.000.
                        </p>
