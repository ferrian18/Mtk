                    <h5>Pembagian</h5>
                    <p>
                        Pembagian merupakan lawan dari operasi perkalian. Pembagian dari 2 bilangan akan mendapatkan
                        hasil bagi atau quotient. <br><br>
                        Pembagian biasanya di analogikan sebagai suatu benda yang kemudian dibagikan kepada beberapa
