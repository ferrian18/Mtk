                    <h5>Penjumlahan</h5>
                    <p>
                        Penjumlahan merupakan penambahan dua bilangan menjadi sebuah bilangan yang merupakan jumlahan
                        dari dua bilangan tersebut. <br><br>
                        Operasi penjumlahan ini dinotasikan dengan simbol + (tambah).
                    </p>
                </div>
                <div class="row">
                    <h5>Pengurangan</h5>
                    <p>
                        Pengurangan merupakan lawan dari operasi penjumlahan. Pengurangan dapat diartikan sebagai
                        perbedaan yang ada diantara dua buah bilangan. <br><br>
                        Operasi pengurangan dinotasikan dengan simbol – (kurang).
                        Misalkan terdapat bilangan A dan B, lalu kita cari perbedaan atau selisihnya: <br>
                        <li>apabila hasil A – B negatif maka nilai A lebih kecil dari nilai B,</li>
                        <li>apabila hasilnya sama dengan nol maka nilai A sama dengan nilai B,</li>
                        <li>apabila hasilnya positif, maka nilai A lebih besar dari nilai B.</li>
                    </p>
                </div>
